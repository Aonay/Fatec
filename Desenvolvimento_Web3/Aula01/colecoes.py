# arrays em PY é divido em coleções: 
# listas -> alteraveis ordenados e pode conter valores repetidos feito com []

lista=["ex1","ex1","ex1","ex1","ex1"]
print(lista)


# tuplas -> coleção de dados ordenados imultaveis feito com parenteses ()

tupla=("ex2","ex1","ex1","ex1","ex1")
print(tupla)
# conjunto -> não ordenado, imultavel e nao indexada não aceita dados duplicados {}

conjunto={"amor","vida","odio","alegria","amor"}
print(conjunto)
# dicionario -> similar ao array assosiativo {}
